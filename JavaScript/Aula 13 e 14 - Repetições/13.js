// Repetições - while - testa e executa o bloco
var a = 1

while (a < 6) {
    console.log('Não.')
    a++
}


// Repetições - do/while - executa e testa o bloco
var b = 1

do {
    console.log('Talvez.') 
    b++
} while(b < 6) 


// Repetiçbões - for
//for(variável; teste lógico; incremento)
for(var d = 1; d < 5; d++) {
    console.log('Sim.')
}
